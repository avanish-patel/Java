package com.basics.java;

public class SortHandOperator {
	
	// +=, -+,........
	
	
	public static void main(String[] args) {
		
		
		int i = 2;
		
		
		i += 2;
		
		System.out.println(i);
		
		i -= 2;
		
		System.out.println(i);
		
		i *= 12;
		
		System.out.println(i);
		
		i /= 2;
		
		System.out.println(i);
		
		i %=2;
		
		System.out.println(i);
	}

}
