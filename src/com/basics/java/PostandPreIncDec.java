package com.basics.java;

public class PostandPreIncDec {

	
	public static void main(String[] args) {
		
		int i = 5;
		
		
		// post increment
		
		int j = i++;
		
		System.out.println(j);
	    
		System.out.println(i);
		
		// pre increment
		
		int k = ++i;
		
		System.out.println(k);
		
		
		// pre decrement
		
		int l = --i;
		
		System.out.println(l);
		
		// post decrement
		
		int m = i--;
		
		System.out.println(m);
		
		System.out.println(i);
		
		

		
	}
	
	
}
